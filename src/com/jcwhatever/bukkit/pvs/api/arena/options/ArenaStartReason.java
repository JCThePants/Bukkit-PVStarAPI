package com.jcwhatever.bukkit.pvs.api.arena.options;

/**
 * Describes the reason an arena is starting.
 */
public enum ArenaStartReason {

    /**
     * The arena is being automatically started.
     */
    AUTO,

    /**
     * The arena is being started in response to all players being ready.
     */
    PLAYERS_READY
}
